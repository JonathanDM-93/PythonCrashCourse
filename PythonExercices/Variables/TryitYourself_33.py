# 2.8 Crear una variable que almacene una operacion aritmetica que devuelva un valor
mySum = 9 + 1 + 3
myRest = 11 - 1
myDiv = 1234 / 10

# Guarde los resultados en una lista
simpleList = [mySum, myRest, myDiv]
# Los pase a traves de un for para imprimir el resultado
for i in simpleList:
    print("Resultado: " + str(i))

